-- Tipos de comandos SQL:

-- 1 - DDL (Data Definition Language): São comandos utilizados para criar e fazer a manutenção de componentes do banco de dados, como a criação de tabelas, índices, entidades, etc...

-- Alguns comandos DDL são: "create" (cria uma tabela), "alter" (altera propriedades do banco de dados), "truncate" (apaga definitivamente os dados de uma tabela), "drop" (apaga a estrutura inteira de uma tabela) e "rename" (renomeia componentes do banco).

-- 2 - DML (Data Manipulation Language): Comandos utilizados para gerenciar os dados do banco.

-- Alguns comandos DML são: "insert" (insere dados na tabela), "update" (altera dados de uma tabela), "delete" (remove dados de uma tabela) e "lock" (gerencia a concorrência da atualização de dados em uma tabela)

-- 3 - DCL (Data Constrol Language): Comandos que permitem administrar o banco de dados, envolvendo questões como segurança, usuários, etc.

-- Alguns comandos DCL são: "commit" (salva o estado atual do banco), "rollback" (retorna o estado salvo previamente do banco) e "savepoint" (salva o estado do banco de forma temporária)